package cn.hzxy.bean;

public class AdvertResult {

	private String alt;
	private String href;
	private String src;
	private String srcB;
	private Double height;
	private Double heightB;
	private Double width;
	private Double widthB;

	public String getAlt() {
		return alt;
	}

	public void setAlt(String alt) {
		this.alt = alt;
	}

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}

	public String getSrc() {
		return src;
	}

	public void setSrc(String src) {
		this.src = src;
	}

	public String getSrcB() {
		return srcB;
	}

	public void setSrcB(String srcB) {
		this.srcB = srcB;
	}

	public Double getHeight() {
		return height;
	}

	public void setHeight(Double height) {
		this.height = height;
	}

	public Double getHeightB() {
		return heightB;
	}

	public void setHeightB(Double heightB) {
		this.heightB = heightB;
	}

	public Double getWidth() {
		return width;
	}

	public void setWidth(Double width) {
		this.width = width;
	}

	public Double getWidthB() {
		return widthB;
	}

	public void setWidthB(Double widthB) {
		this.widthB = widthB;
	}
}
